  let resumeCounter = 0;

  window.addEventListener('load', ()=>{
    document.querySelector('.previous-button').addEventListener('click', previousResume);
    document.querySelector('.next-button').addEventListener('click', nextResume);
    document.querySelector('.search-button').addEventListener('click', searchResume);
    disableButtons(true, false);
  })

 
  function searchResume(event){
    let searchText = document.querySelector('.search').value.trim();
    let copyOrifinalData = [...resumeData.resume];
    if(searchText){
        let filteredData = resumeData.resume.filter((obj)=>{
            return searchText.toLowerCase() === obj.basics.AppliedFor.toLowerCase();
        });
        resumeData.resume = [...filteredData];
        createResumeData(0);

    }else{
        window.location.reload();
    }
  }

  function nextResume(){
    if(resumeCounter < resumeData.resume.length-1){
        createResumeData(++resumeCounter);
        disableButtons(false, false);
    }else{
        disableButtons(false, true);
    }
  }

  function previousResume(){
    if(resumeCounter >= 0){
        createResumeData(resumeCounter);
        disableButtons(false, false);
        resumeCounter--;
    }else{
        disableButtons(true, false);
    }
  }

  function disableButtons(disablePrevious, disableNext){
    let prevButton =  document.querySelector('.previous-button');
    let nextButton = document.querySelector('.next-button');
    prevButton.disabled = disablePrevious;
    nextButton.disabled = disableNext;
  }

  function deleteChildNodesIfAlreadyPresent(nodeRef){
    while (nodeRef.hasChildNodes()) {
        nodeRef.removeChild(nodeRef.firstChild);
      }
  }

